def prompt(label, default=None, required=False, secret=False):
    if default is not None:
        display = f"  {label} [{default}]: "
    elif required:
        display = f"  {label} (required): "
    else:
        display = f"  {label}: "

    while True:
        if secret:
            import getpass
            value = getpass.getpass(display)
        else:
            value = input(display).strip()

        if value:
            return value
        elif default is not None:
            return str(default)
        elif required:
            print("    This field is required.")
        else:
            return ""


def prompt_yn(label, default="y"):
    suffix = "[Y/n]" if default == "y" else "[y/N]"
    while True:
        value = input(f"  {label} {suffix}: ").strip().lower()
        if value == "":
            return default == "y"
        if value in ("y", "yes"):
            return True
        if value in ("n", "no"):
            return False
        print("    Please enter y or n.")


def prompt_choice(label, choices):
    print(f"\n  {label}")
    for key, (name, _) in choices.items():
        print(f"    {key}) {name}")
    print()
    while True:
        value = input("  Choose [1]: ").strip()
        if value == "":
            return choices["1"]
        if value in choices:
            return choices[value]
        print("    Invalid choice.")


