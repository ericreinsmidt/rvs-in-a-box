def write_compose(config):
    lines = []
    lines.append("services:")
    lines.append("  ravenshield:")
    lines.append(f"    image: {REGISTRY}/rvs-server:latest")
    lines.append("    container_name: rvs-server")
    lines.append("    restart: unless-stopped")
    lines.append("    ports:")
    lines.append('      - "7777:7777/udp"')
    lines.append(f'      - "{config["server_beacon_port"]}:{config["server_beacon_port"]}/udp"')
    lines.append('      - "9777:9777/udp"')
    lines.append("    env_file:")
    lines.append("      " + chr(45) + chr(32) + ".env")
    lines.append("    volumes:")
    lines.append("      - redirect-files:/srv/redirect")
    lines.append("")
    lines.append("  rvsdash:")
    lines.append(f"    image: {REGISTRY}/rvs-dash:latest")
    lines.append("    container_name: rvs-dash")
    lines.append("    restart: unless-stopped")
    lines.append("    ports:")
    lines.append(f'      - "{config["rvsdash_port"]}:2003"')
    lines.append("    volumes:")
    lines.append("      - rvsdash-data:/app/app/data")
    lines.append("    env_file:")
    lines.append("      " + chr(45) + chr(32) + ".env")
    lines.append("    environment:")
    lines.append("      - RVS_ADMIN_PASSWORD=${N4ADMIN_PASSWORD}")
    lines.append("      - DEFAULT_SERVER_IP=rvs-server")
    lines.append("      - DEFAULT_SERVER_PORT=${SERVER_BEACON_PORT:-8777}")
    lines.append("      - DEFAULT_SERVER_IDENT=${SERVER_IDENT:-rvs1}")
    lines.append("      - SITE_TITLE=${SITE_TITLE}")
