    lines.append("      - FOOTER_HTML=${FOOTER_HTML}")
    lines.append("    depends_on:")
    lines.append("      - ravenshield")
    lines.append("")
    lines.append("  redirect:")
    lines.append("    image: caddy:2-alpine")
    lines.append("    container_name: rvs-redirect")
    lines.append("    restart: unless-stopped")
    lines.append("    ports:")
    lines.append('      - "80:80"')
